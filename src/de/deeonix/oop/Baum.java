package de.deeonix.oop;

public class Baum {
    private int groesse;
    private int farbe;

    public Baum(int groesse, int farbe) {
        this.groesse = groesse;
        this.farbe = farbe;
    }

    public int getGroesse() {
        return groesse;
    }

    public int getFarbe() {
        return farbe;
    }
}
